    /**
     * Downloads a course pdf.
     *
     * @return \Illuminate\Http\Response
     */

    public function downloadPDF($id) {
        $courses = Course::find($id);
        $pdf = PDF::loadView('courses.pdf', compact('courses'));
        
        return $pdf->download('course.pdf');
    }

